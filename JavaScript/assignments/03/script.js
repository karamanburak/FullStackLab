const number = prompt("Enter a Month name")
let result

switch (number) {
    case 'January':
        result = "1"
        break
    case 'february':
        result = "2"
        break
    case 'march':
        result = "3"
        break
    case 'april':
        result = "4"
    case 'may':
        result = "5"
        break
    case 'juni':
        result = "6"
        break
    case 'juli':
        result = "7"
        break
    case 'august':
        result = "8"
        break
    case 'september':
        result = "9"
        break
    case 'october':
        result = "10"
        break
    case 'november':
        result = "11"
        break
    case 'december':
        result = "13"
        break
    default:
        result = "Please enter a valid month name!"
}


console.log(`Your lucky month is : ${result}`)


